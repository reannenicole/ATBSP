# In python you can define your own functions using the def() function
# In the below, Hello is the definition of this function, and the parameter is name
# The parameter of a def() is a variable that the argument is stored in when the function is called. 
# The argument is defined in line 8, which automatically sets the variable in the function to "Alice"

def hello(name):
  print('Hello ' + name)
hello('Alice')
hello('Bob')

